public class Calc {

  //divide two numbers
  public int divide(int a, int b) {
    return a / b;
  }

  //multiply two numbers
  public int multiply(int a, int b) {
    return a * b;
  }
  // add two numbers

  public int add(int a, int b) {
    return a + b;
  }
  
  // subtract two numbers
  public int subtract(int a, int b) {
    return a - b;
  }

}
